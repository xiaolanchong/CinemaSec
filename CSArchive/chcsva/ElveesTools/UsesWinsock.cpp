///////////////////////////////////////////////////////////////////////////////
// CUsesWinsock
///////////////////////////////////////////////////////////////////////////////

CUsesWinsock::CUsesWinsock()
{
	WORD wVersionRequested = 0x202;

	if(0 != ::WSAStartup(wVersionRequested, &m_data))
	{
		throw CWin32Exception(TEXT("CUsesWinsock::CUsesWinsock()"), ::WSAGetLastError());
	}
}
      
CUsesWinsock::~CUsesWinsock()
{
	WSACleanup();
}

