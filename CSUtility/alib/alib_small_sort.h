/** \brief Function sorts five digits in ascendant order, using Shell's algorithm.

  \param  x     the pointer to an array to be sorted.
  \param  size  the size of an array, must be equal to 5. */
//-------------------------------------------------------------------------------------------------
template< class T >
inline void AscendantSortFive( T * x, int size )
{
  ASSERT( size == 5 );size;
  T t;
  if (x[0] > x[2]) { t = x[0];  x[0] = x[2];  x[2] = t; }
  if (x[1] > x[3]) { t = x[1];  x[1] = x[3];  x[3] = t; }
  if (x[2] > x[4]) { t = x[2];  x[2] = x[4];  x[4] = t;
  if (x[0] > x[2]) { t = x[0];  x[0] = x[2];  x[2] = t; }}
  if (x[0] > x[1]) { t = x[0];  x[0] = x[1];  x[1] = t; }
  if (x[1] > x[2]) { t = x[1];  x[1] = x[2];  x[2] = t;
  if (x[0] > x[1]) { t = x[0];  x[0] = x[1];  x[1] = t; }}
  if (x[2] > x[3]) { t = x[2];  x[2] = x[3];  x[3] = t;
  if (x[1] > x[2]) { t = x[1];  x[1] = x[2];  x[2] = t;
  if (x[0] > x[1]) { t = x[0];  x[0] = x[1];  x[1] = t; }}}
  if (x[3] > x[4]) { t = x[3];  x[3] = x[4];  x[4] = t;
  if (x[2] > x[3]) { t = x[2];  x[2] = x[3];  x[3] = t;
  if (x[1] > x[2]) { t = x[1];  x[1] = x[2];  x[2] = t;
  if (x[0] > x[1]) { t = x[0];  x[0] = x[1];  x[1] = t; }}}}
}


